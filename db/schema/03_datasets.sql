DROP TABLE IF EXISTS users CASCADE;
CREATE TABLE datasets (
  id SERIAL PRIMARY KEY NOT NULL,
  user_id INTEGER REFERENCES users(id) ON DELETE CASCADE,
  company_name VARCHAR(255) NOT NULL DEFAULT 0,
  sales_revenue INTEGER NOT NULL DEFAULT 0,
  cogs INTEGER NOT NULL DEFAULT 0,
  sga_expenses INTEGER NOT NULL DEFAULT 0,
  interest_expense INTEGER NOT NULL DEFAULT 0,
  income_tax_expense INTEGER NOT NULL DEFAULT 0,
);